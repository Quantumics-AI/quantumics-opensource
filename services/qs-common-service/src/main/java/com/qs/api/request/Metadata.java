import lombok.NoArgsConstructor;

@Data
@NoArgsConstructor
@AllArgsConstructor
@JsonIgnoreProperties(ignoreUnknown = true)
public class Metadata {
  @JsonProperty("key") private String key;
  @JsonProperty("value") private String value;
}
